/**
 * Get a user by ID
 */
export function getUserById(userId: string): User | undefined {
  return mockUsers.find((u) => u.id === userId)
}

/**
 * Get users by department
 */
export function getUsersByDepartment(department: string): User[] {
  return mockUsers.filter((u) => u.department === department)
}

/**
 * Get users by location (checks role scopes)
 */
export function getUsersByLocation(locationId: string): User[] {
  return mockUsers.filter((u) =>
    u.roleAssignments.some((ra) =>
      ra.scopes.some((s) => s.locationId === locationId)
    )
  )
}

/**
 * Get all unique departments from users
 */
export function getAllDepartments(): string[] {
  const departments = new Set(mockUsers.map((u) => u.department).filter(Boolean))
  return Array.from(departments).sort() as string[]
}

/**
 * Create a mock user with random data (for generating additional users)
 */
export function createMockUser(
  id: string,
  firstName: string,
  lastName: string,
  options: Partial<User> = {}
): User {
  const email = `${firstName.toLowerCase()}.${lastName.toLowerCase()}@acme.com`
  return {
    id,
    email,
    firstName,
    lastName,
    jobTitle: options.jobTitle ?? 'Employee',
    department: options.department ?? 'Operations',
    status: options.status ?? 'active',
    roleAssignments: options.roleAssignments ?? [],
    createdAt: options.createdAt ?? new Date().toISOString(),
    ...options,
  }
}
