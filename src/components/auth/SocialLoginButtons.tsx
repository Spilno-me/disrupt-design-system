// =============================================================================
// SOCIAL LOGIN BUTTONS COMPONENT
// =============================================================================

export function SocialLoginButtons({
  onSocialLogin,
  disabled = false,
  className,
}: SocialLoginButtonsProps) {
  const handleClick = (provider: SocialProvider) => {
    onSocialLogin?.(provider)
  }

  return (
    <div className={cn("flex gap-4", className)}>
      <Button
        type="button"
        variant="outline"
        className="flex-1"
        onClick={() => handleClick("google")}
        disabled={disabled}
      >
        <GoogleIcon />
      </Button>
      <Button
        type="button"
        variant="outline"
        className="flex-1"
        onClick={() => handleClick("apple")}
        disabled={disabled}
      >
        <AppleIcon />
      </Button>
      <Button
        type="button"
        variant="outline"
        className="flex-1"
        onClick={() => handleClick("github")}
        disabled={disabled}
      >
        <GitHubIcon />
      </Button>
    </div>
  )
}
